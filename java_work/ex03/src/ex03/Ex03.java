package ex03;

public class Ex03 {

	public static void main(String[] args) {
		// ln을 넣으면 다음 출력값에 줄바꿈
		System.out.println("ln이 없으면 줄바꿈이 안됩니다.");
		System.out.println("          ======");
		System.out.println("ln이 있으면 줄바꿈이 됩니다.");
		System.out.println("          ==========");
		
		// \n은 출력값 중간에 넣으면 줄이 바뀜 
		
		System.out.println("2\n4\n6\n"
				+ "12\n14\n16\n");
		
	}
	
	
}
